import pandas as pd

def summarize_scores():
    df1 = pd.read_csv("/content/outputs/scoring/part1_single_panel.csv")
    df2 = pd.read_csv("/content/outputs/scoring/part2_cross_panel.csv")

    score_cols1 = [
        "dog_color_correct",
        "dog_breed_correct",
        "scene_correct",
        "prop_present",
        "action_correct"
    ]

    score_cols2 = [
        "dog_color_consistent",
        "dog_appearance_consistent",
        "scene_consistent",
        "story_coherent",
        "props_continuous"
    ]

    # Convert to numeric
    df1[score_cols1] = df1[score_cols1].apply(pd.to_numeric, errors="coerce")
    df2[score_cols2] = df2[score_cols2].apply(pd.to_numeric, errors="coerce")

    # Compute scores
    df1["single_score"] = df1[score_cols1].sum(axis=1)
    df2["cross_score"] = df2[score_cols2].sum(axis=1)

    print("=" * 50)
    print("Part 1: Single-Panel Accuracy (Max Score = 5)")
    print("=" * 50)

    summary1 = df1.groupby("experiment")["single_score"].agg(["mean", "std"]).round(2)
    print(summary1)

    print("\nDetailed Metrics:")
    detail1 = df1.groupby("experiment")[score_cols1].mean().round(2)
    print(detail1)

    print("\n" + "=" * 50)
    print("Part 2: Cross-Panel Consistency (Max Score = 5)")
    print("=" * 50)

    summary2 = df2.groupby("experiment")["cross_score"].agg(["mean", "std"]).round(2)
    print(summary2)

    print("\nDetailed Metrics:")
    detail2 = df2.groupby("experiment")[score_cols2].mean().round(2)
    print(detail2)

    # Save summaries
    summary1.to_csv("/content/outputs/scoring/summary_part1.csv")
    summary2.to_csv("/content/outputs/scoring/summary_part2.csv")

    print("\nSummaries saved to /content/outputs/scoring/")


summarize_scores()

