def build_structured_prompts(config):
    return {
        panel_id: template.format(**config)
        for panel_id, template in PANEL_TEMPLATES.items()
    }

structured_prompts = build_structured_prompts(DOG_CONFIG)
baseline_prompts = {i: BASELINE_PROMPT for i in range(1, 5)}

