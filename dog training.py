pipe = StableDiffusionPipeline.from_pretrained(
    "runwayml/stable-diffusion-v1-5",
    torch_dtype=torch.float16,
)
pipe = pipe.to("cuda")
pipe.enable_attention_slicing()
print("Model loaded.")

def generate_image(pipe, prompt, negative_prompt, seed):
    generator = torch.Generator("cuda").manual_seed(seed)
    result = pipe(
        prompt=prompt,
        negative_prompt=negative_prompt,
        num_inference_steps=30,
        guidance_scale=7.5,
        width=512,
        height=512,
        generator=generator,
    )
    return result.images[0]

def make_grid(images, labels, title):
    """Create a 1-row image grid with title and panel labels"""
    W, H = 512, 512
    label_h = 36
    title_h = 48
    padding = 12
    cols = len(images)

    total_w = W * cols + padding * (cols + 1)
    total_h = title_h + label_h + H + padding * 2

    grid = Image.new("RGB", (total_w, total_h), (245, 245, 245))
    draw = ImageDraw.Draw(grid)

    # Draw title
    draw.text((total_w // 2, title_h // 2), title,
              fill=(30, 30, 30), anchor="mm")

    for i, (img, label) in enumerate(zip(images, labels)):
        x = padding + i * (W + padding)
        y = title_h + label_h

        # Draw panel label
        draw.text((x + W // 2, title_h + label_h // 2), label,
                  fill=(80, 80, 80), anchor="mm")

        grid.paste(img, (x, y))

    return grid

def run_experiment(pipe, prompts, negative_prompt, n_runs, exp_name):
    os.makedirs(f"/content/outputs/{exp_name}/grids", exist_ok=True)
    os.makedirs(f"/content/outputs/{exp_name}/panels", exist_ok=True)

    all_results = {}

    for run_id in range(n_runs):
        print(f"\n[{exp_name}] Run {run_id + 1}/{n_runs}")
        panels = {}
        images = []

        for panel_id in range(1, 5):
            seed = (run_id + 1) * 100 + panel_id
            print(f"  Generating Panel {panel_id} (seed={seed})...")
            img = generate_image(pipe, prompts[panel_id], negative_prompt, seed)

            path = f"/content/outputs/{exp_name}/panels/run{run_id+1}_panel{panel_id}.png"
            img.save(path)
            panels[panel_id] = img
            images.append(img)

        # Create 4-panel grid image
        title = f"{exp_name.upper()} — Run {run_id + 1}"
        labels = [PANEL_LABELS[i] for i in range(1, 5)]
        grid = make_grid(images, labels, title)
        grid_path = f"/content/outputs/{exp_name}/grids/run{run_id+1}_grid.png"
        grid.save(grid_path)

        all_results[run_id + 1] = panels
        print(f"  Grid saved: {grid_path}")

        # Display in Colab
        display(grid)

    return all_results

